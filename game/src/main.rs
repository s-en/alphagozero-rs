fn main() {
  let mut x: u32 = 0b11111_11111_11111_00000_00000;
  let mut t: u32;
  const K1: u32 = 0b11000_11000_00000_00000_00000;
  const K2: u32 = 0b00100_00000_10000_00000_00000;
  const K3: u32 = 0b10010_00100_01000_10010_00000;
  t  = K1 & (x ^ (x << 18));
  x ^=       t ^ (t >> 18) ;
  t  = K2 & (x ^ (x << 12));
  x ^=       t ^ (t >> 12) ;
  t  = K3 & (x ^ (x <<  6));
  x ^=       t ^ (t >>  6) ;
  println!("{:b}", x);
}
