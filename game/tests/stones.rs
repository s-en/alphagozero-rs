
use az_game::igo::*;

#[cfg(test)]
mod stones {
  use super::*;

  #[test]
  fn add_stone64() {
    let b = Stones::new64(0b0000000_1111111_1111111_1111111_1111111_1111111_1111111);
    assert_eq!(
      format!("{}", b + 1),
      "-------\n\
      -------\n\
      -------\n\
      -------\n\
      -------\n\
      -------\n\
      o------\n"
    );
  }
  #[test]
  fn add_stone128() {
    let b = Stones::new128(
      0b000000000_111111111_111111111_111111111_111111111_111111111_111111111_111111111_111111111,
    );
    assert_eq!(
      format!("{}", b + 1),
      "---------\n\
      ---------\n\
      ---------\n\
      ---------\n\
      ---------\n\
      ---------\n\
      ---------\n\
      ---------\n\
      o--------\n"
    );
  }
}