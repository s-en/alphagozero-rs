  pub fn predict_tensor(&self, board: Tensor) -> (Vec<f32>, f32) {
    let b = board.view([12, self.board_size, self.board_size]).totype(Kind::Float);
    let pi: Tensor;
    let v: Tensor;
    if let Some(model) = &self.model {
      let output = model.forward_is(&[IValue::from(b)]).unwrap();
      let (o1, o2) = match output {
        IValue::Tuple(ivalues) => match &ivalues[..] {
            [IValue::Tensor(t1), IValue::Tensor(t2)] => (t1.shallow_clone(), t2.shallow_clone()),
            _ => panic!("unexpected output {:?}", ivalues),
        },
        _ => panic!("unexpected output {:?}", output),
      };
      pi = o1;
      v = o2;
    } else if let Some(model) = &self.tmodel {
      let output = model.forward_is(&[IValue::from(b)]).unwrap();
      let (o1, o2) = match output {
        IValue::Tuple(ivalues) => match &ivalues[..] {
            [IValue::Tensor(t1), IValue::Tensor(t2)] => (t1.shallow_clone(), t2.shallow_clone()),
            _ => panic!("unexpected output {:?}", ivalues),
        },
        _ => panic!("unexpected output {:?}", output),
      };
      pi = o1;
      v = o2;
    } else {
      panic!("predict_tensor model not found");
    }
    let rs1 = pi.view([self.action_size]);
    let rs2 = v.view([1]);
    let r1 = Vec::<f32>::from(rs1);
    let r2 = rs2.double_value(&[0]) as f32;
    (r1, r2)
  }
