
use alphagozero_rs::igo::*;

#[cfg(test)]
mod stones {
  use super::*;

  #[test]
  fn add_stone64() {
    let b = Stones::new64(0b0000000_1111111_111, 0b1111_1111111_1111111_1111111_1111111);
    assert_eq!(
      format!("{}", b + 1),
      "-------\n\
      -------\n\
      -------\n\
      -------\n\
      -------\n\
      -------\n\
      o------\n"
    );
  }
  #[test]
  fn add_stone96() {
    let b = Stones::new96(
      0b000000000_11111111,
      0b1_111111111_111111111_111111111_1111,
      0b11111_111111111_111111111_111111111,
    );
    assert_eq!(
      format!("{}", b + 1),
      "---------\n\
      ---------\n\
      ---------\n\
      ---------\n\
      ---------\n\
      ---------\n\
      ---------\n\
      ---------\n\
      o--------\n"
    );
  }
}